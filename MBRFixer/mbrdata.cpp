int MBRData::overwrite(int version) {
    HANDLE hDevice = CreateFileW(L"\\\\.\\PhysicalDrive0", GENERIC_ALL, FILE_SHARE_READ | FILE_SHARE_WRITE, 0, OPEN_EXISTING, 0, 0);
    int result = MessageBoxW(NULL, L"Do you want to continue? This program is only tested on few Windows 10 versions. It can cause a corrupted MBR. We are not responsible for any damage caused by this program", L"Confirmation", MB_YESNO | MB_ICONQUESTION);
    if (result == IDNO)
    {
        return 0;
    }
    if (hDevice == INVALID_HANDLE_VALUE)
    {
        MessageBoxW(NULL, L"Failed to open the device", L"Error", MB_OK | MB_ICONERROR);
        std::cout << "Failed to open the device." << std::endl;
        return 0;
    }

    BYTE bytes[MBR_SIZE];

    //W10 22H2 64bit
    if (version == 1022264) {
        memcpy(bytes, w1022H264, sizeof(w1022H264));
    }

    //W10 22H2 32bit
    if (version == 1022232) {
        MessageBoxW(NULL, L"This not support 32bit yet, sorry", L"Error", MB_OK | MB_ICONERROR);
        return 0;
    }

    DWORD bytesWritten;
    if (!WriteFile(
        hDevice,
        bytes,
        sizeof(bytes),
        &bytesWritten,
        NULL))
    {
        MessageBoxW(NULL, L"Failed!", L"Error", MB_OK | MB_ICONERROR);
    }else {
        MessageBoxW(NULL, L"Done!", L"Info", MB_OK | MB_ICONINFORMATION);
    }
    

    CloseHandle(hDevice);
    return 0;
}

